package org.xpdojo.search.dto;

import java.math.BigInteger;

public class Product {

    private String id;
    private String name;
    private BigInteger price;

    public Product(String id, String name, BigInteger price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public BigInteger getPrice() {
        return price;
    }

    @Override
    public String toString() {
        return "Product{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", price=" + price +
                '}';
    }
}
